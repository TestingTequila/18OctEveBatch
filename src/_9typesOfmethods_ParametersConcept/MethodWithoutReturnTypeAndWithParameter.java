public class MethodWithoutReturnTypeAndWithParameter {

	// methodWithoutReturnType+methodWithParameter
	
	
	
	public void addition(int a,int b  ) 
	{
		 int sum=a+b;
		 System.out.println("Addition with Parameters " +sum);
	}
	
	public void subtraction(int a, int b ) 
	{
		 int diff=a-b;
		 System.out.println("Subtraction with Parameters " +diff);
	}
	
	public void multiplication()
	{
		int a=4;
		int b=5;
		int product =a*b;
		System.out.println("Mulriplication as local variable : " + product);
	}
	


}
